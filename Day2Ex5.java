
public class Day2Ex5 {
public static void main(String[]args)
{
int a = 9;

a++;

System.out.println (a);
a--;

System.out.println (a);

++a;

System.out.println (a);

System.out.println (a++);
System.out.println (a);

System.out.println (a--);

System.out.println (--a);

System.out.println (++a);
}
}